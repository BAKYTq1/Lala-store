document.querySelector('.img').addEventListener('click', function () {
    const menu = document.querySelector('.dropdown-menu');
    if (menu.style.display === 'none' || menu.style.display === '') {
      menu.style.display = 'block'; 
    } else {
      menu.style.display = 'none';
    }
  });
   
